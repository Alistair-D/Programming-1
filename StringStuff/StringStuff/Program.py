thing = "HELLO world HERE is SOME text"
print(thing.lower())           #No Capitals
print(thing.upper())           #All letters are Cap.
print(thing.capitalize())      #Cap. first letter only
print(thing.title())           #Cap. Every first Letter in each Word
print(thing.isdigit())
print(thing.find("world"))     #Also see index and rindex
print(thing.count("E"))
print(thing[::-1])             #Reverse
print(thing.split(" "))
